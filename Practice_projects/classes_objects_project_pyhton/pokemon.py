class Pokemon:
  def __init__(self,entry,name,types,description,is_caught):
    self.entry=entry
    self.name=name
    self.types=types
    self.description=description
    self.is_caught=is_caught
  
  def speak(self):
    print(f"Pokemon {self.name} can speak {self.name}! {self.name}!")
  def display_details(self):
    print(f"Entry Number:{self.entry} \n Name:{self.name} \n Type:{self.types} \n Description:{self.description}")

