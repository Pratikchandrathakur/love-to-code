# For loops - start to finish of an iterate
vegetables = ["cucumber", "spinach", "cabbage"]
for veggies in vegetables:
    print(veggies)

for i in range(5):
    print(i)

word = "Python"
for letter in word:
    print(letter)

# While loops - execute as long as True
i = 1

while i < 10:
    print(i)
    i += 1

